<?php
    // 新建陣列變數時,在間隔中輸入數字20=>1
    // 會先跑[0]然後從[20]開始跳...[21]..[22]...
    $ary1 =array(0,20=> 1,2,3,4,5,6);
    var_dump($ary1);
    echo "<hr/>";
    // 當更改index數時,有指向數個值時以數字最大的為主
    // 以下範例:會先跑[0],[20],[21],[10],[22]因為10小於20
    $ary2 =array(0,20=> 1,2,10=> 3,4,5,6);
    var_dump($ary2);
    echo "<hr/>";
    // 陣列中也可以將字串塞進當index
    // 若將false=>指向index值時會整段消失
    $ary3 =array(0,20=> 1,2,'test'=> 3,4,5,6);
    var_dump($ary3);


?>